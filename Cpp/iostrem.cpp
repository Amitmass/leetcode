#include<iostream>
using namespace std;

int main(){
    int num1, num2;
    cout<<"Enter the value for num 1: "; // '<<' is the Insertion operator
    cin>>num1; // '>>' is Extraction operator
    cout<<"Enter the value for num 2: "; // '<<' is the Insertion operator
    cin>>num2; // '>>' is Extraction operator
    cout<<"Sum of num1 and num2: "<<num1 + num2;
    return 0;

}